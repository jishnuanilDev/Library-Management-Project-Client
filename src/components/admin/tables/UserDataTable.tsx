export default function UserDataTable() {
  const dispatch = useDispatch();
  const users = useSelector((state: any) => state.user.users);


  React.useEffect(() => {
    if (users.length === 0) { 
      const fetchUsers = async () => {
        try {
          const response = await axiosInstance.get("/fetch-users");
          dispatch(setUsers(response.data.users));

        } catch (error) {
          console.error("Error fetching users:", error);
        }
      };
      fetchUsers();
    }
  }, [users.length, dispatch]);

  const [page, setPage] = React.useState(0);

  const [rowsPerPage, setRowsPerPage] = React.useState(10);

  const handleChangePage = (event: unknown, newPage: number) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (
    event: React.ChangeEvent<HTMLInputElement>
  ) => {
    setRowsPerPage(+event.target.value);
    setPage(0);
  };

  return (
    <>
      <Paper sx={{ width: "100%", overflow: "hidden" }}>
        <TableContainer sx={{ maxHeight: 440 }}>
          <Table stickyHeader aria-label="sticky table">
            <TableHead>
              <TableRow>
                <TableCell sx={{ fontWeight: "bold", fontStyle: "italic" }}>Username</TableCell>
                <TableCell sx={{ fontWeight: "bold", fontStyle: "italic" }}>Email</TableCell>
                <TableCell sx={{ fontWeight: "bold", fontStyle: "italic" }}>Action</TableCell>{" "}
                {/* Column header for the action */}
              </TableRow>
            </TableHead>
            <TableBody>

              {
                users.map((user,index)=>(
                  <TableRow hover role="checkbox" tabIndex={-1}>
                          <TableCell>{user.username}</TableCell>
                          <TableCell>{user.email}</TableCell>
                       
                  <TableCell>
                    <Button
                    disabled
                      variant="outlined"
                      color="primary"
                      sx={{ marginRight: 1 }}
                    >
                      Edit
                    </Button>
  
                    <Button disabled variant="outlined" color="error">
                      Delete
                    </Button>
                  </TableCell>
                </TableRow>
                ))
              }
       
            </TableBody>
          </Table>
        </TableContainer>
        <TablePagination
          rowsPerPageOptions={[10, 25, 100]}
          component="div"
          count={rows.length}
          rowsPerPage={rowsPerPage}
          page={page}
          onPageChange={handleChangePage}
          onRowsPerPageChange={handleChangeRowsPerPage}
        />
      </Paper>
    </>
  );
}
